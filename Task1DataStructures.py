#list of my favorite fruits
fruits = ['mango', 'strawberry', 'pineapple', 'grape', 'orange']
print("Original list:", fruits)

# appending a new fruit
fruits.append('kiwi')
print("Added:", fruits)

# removing one fruit from the list
fruits.remove('grape')
print("removed:", fruits)

# print list
reversed_fruits = fruits[::-1]
print("Reversed list:", reversed_fruits)
